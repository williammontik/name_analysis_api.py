SMTP_SERVER = "smtp.gmail.com"
SMTP_PORT = 587
SMTP_USERNAME = "kata.chatbot@gmail.com"
SMTP_PASSWORD = os.getenv("SMTP_PASSWORD")

def send_email_with_charts(html_body, chart_images):
    try:
        msg = MIMEMultipart('related')
        msg["Subject"] = "New KataChatBot Submission"
        msg["From"] = SMTP_USERNAME
        msg["To"] = SMTP_USERNAME

        msg_alt = MIMEMultipart('alternative')
        msg.attach(msg_alt)
        msg_alt.attach(MIMEText(html_body, 'html'))

        # Attach each chart image with content ID
        for idx, base64_img in enumerate(chart_images):
            try:
                header, encoded = base64_img.split(',', 1)
                image_data = base64.b64decode(encoded)
                image = MIMEImage(image_data, name=f"chart{idx+1}.png")
                cid = f"chart{idx+1}"
                image.add_header('Content-ID', f'<{cid}>')
                msg.attach(image)
            except Exception as e:
                app.logger.warning(f"âŒ Failed to attach chart {idx+1}: {e}")

        with smtplib.SMTP(SMTP_SERVER, SMTP_PORT) as server:
            server.starttls()
            server.login(SMTP_USERNAME, SMTP_PASSWORD)
            server.send_message(msg)
        app.logger.info("âœ… Email with charts sent")
    except Exception as e:
        app.logger.error("âŒ Email sending failed", exc_info=True)

